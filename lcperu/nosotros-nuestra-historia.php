                <div class="texto">
                
<p>
Los orígenes de la compañía se remontan al año 1993 cuando fue constituída como empresa de transporte de carga, con el objetivo de brindar servicios de traslado de mercadería por vía aérea; haciendo las veces de Agente de Carga (Freight Foward). Es en el año 1998, que se incursiona en el negocio de línea aérea.
</p>

<p>
Luego de una exhaustiva evaluación, se concluye que el segmento regional estaba muy desatendido, concluyendo que era el posicionamiento que la compañía debería tener; para lo cual se inicia un arduo proceso de certificación como línea aérea. En el año 2001 se inicia las operaciones de vuelos regulares, luego de un proceso de certificación ante la Federal Aviation Administration (FAA - USA) y la Dirección General de Aeronáutica Civil (DGAC - Perú).
</p>
<p>
Luego de los éxitos obtenidos, se decide incorporar la segunda aeronave en el año 2003; incrementando nuestros destinos hacia dos importantes ciudades del Perú. A mediados del 2004 se incorpora la tercera aeronave, y se extienden las rutas a 4 destinos en total. Todas las rutas son atendidas con vuelos diarios. En el año 2005 y luego del éxito obtenido en los vuelos regulares, aperturamos nuestra nueva unidad de negocios de Vuelos Charters; (vuelos privados de pasajeros y carga).
</p>
<p>
Ver Línea de Tiempo
</p>

<p>
<h5>
Compromiso
</h5>
Somos una empresa peruana, con muchos años de experiencia surcando los cielos del Perú, con la tecnología aérea que nuestro territorio exige; llegando a diferentes destinos, ofreciendo flexibilidad en horarios y tarifas.

Cultivando la honestidad, responsabilidad y calidez en el trato a nuestros clientes, reconocemos en ellos personas con necesidades y expectativas distintas; fomentando la confianza con un trato cercano, elevados estándares de seguridad, aprecio a la puntualidad y promesas siempre cumplidas.
</p>

<p>
<h5>
Visión y Misión
</h5>
Ser una de las 5 mejores aerolíneas regionales de América.
</p>


<p>
<h5>
Política Corporativa Integrada
</h5>
Es compromiso de LC Perú lograr operaciones seguras y eficientes cumpliendo con todos los requisitos legales nacionales e internacionales, mediante un Sistema de Gestión de la organización que mejora continuamente su eficacia y su desempeño. Protegemos a las personas, bienes y medio ambiente, satisfaciendo a nuestros clientes y partes involucradas.

Nuestro sistema contempla la Gestión Organizacional, Seguridad Operacional, Seguridad de la Aviación, Seguridad y Salud Ocupacional, así también Seguridad Ambiental con políticas, normas y métodos claros debidamente comunicados y entendidos por todo nuestro personal.

